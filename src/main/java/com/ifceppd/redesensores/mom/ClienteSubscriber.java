    public ClienteSubscriber(Cliente c) throws Exception {
        initComponents();
        
        this.jLabelTitulo.setText(c.getNome());
        
        DefaultListModel d = c.getSensores();
        Object[] lista = d.toArray();
        String topicName;
        
        for (Object s : lista) {
            topicName = s.toString();
            new Subscriber(topicName).Go();
        }

    }
    
    private class Subscriber implements MessageListener{
        /*
         * URL do servidor JMS. DEFAULT_BROKER_URL indica que o servidor está em localhost
        */	

        private static final String url = ActiveMQConnection.DEFAULT_BROKER_URL;

        private Session session;
        private MessageConsumer subscriber;
        private Connection connection;

        public Subscriber(String topicName) throws Exception{
            /*
             * Estabelecendo conexão com o Servidor JMS
            */		
            ConnectionFactory connectionFactory = new ActiveMQConnectionFactory(url);
            Connection connection = connectionFactory.createConnection();
            connection.start();

            /*
            * Criando Session 
            */
            Session session = connection.createSession(false, Session.AUTO_ACKNOWLEDGE);

            /*
            * Criando Topic
            */ 
            Destination dest = session.createTopic(topicName);

            /*
            * Criando Consumidor
            */
            MessageConsumer subscriber = session.createConsumer(dest);

            this.connection = connection;
            this.session = session;
            this.subscriber = subscriber;
        }

        public void Go(){

            try{
                /*
                * Setando Listener
                */
                subscriber.setMessageListener(this);

            }catch(Exception e){
                e.printStackTrace();
            }       
        }

        @Override
        public void onMessage(Message message) {
            if(message instanceof ObjectMessage){
                try{
                    ObjectMessage obj = (ObjectMessage) message;
                    Sensor s = (Sensor) obj.getObject();
                    jTextAreaLog.setText(jTextAreaLog.getText() + "\n" 
                            + s.getNome() + ": " + s.getLeituraAtual());
                    jTextAreaLog.setCaretPosition(jTextAreaLog.getText().length());
                    /*
                   String topicName = "";
                   int index = topicName.indexOf("("); 
                   topicName = topicName.substring(index, topicName.length());*/
                }catch(Exception e){
                }
            }
        }
    
    }
    

